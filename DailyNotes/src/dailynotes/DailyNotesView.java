    private void saveFileJMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFileJMenuItemActionPerformed
        //Open a file open dialog to get user input on what folder and what name to save it.
        JFileChooser saveFileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter( "Text Files", "txt" );

        saveFileChooser.setFileFilter( filter );
        if( saveFileChooser.showSaveDialog(mainPanel) == JFileChooser.APPROVE_OPTION )
        {//If the save button is chosen, save the file.
            String saveFileName = saveFileChooser.getSelectedFile().getAbsolutePath();
            if( !(saveFileName.endsWith(".txt")) )
            {//If the filename doesn't end with .txt, add it to the end.
                saveFileName += ".txt";
            }

            //Call DailyNotesFileIO to save the text.
            DailyNotesFileIO.saveFile( saveFileName, textFileJTextArea.getText() );
        }
    }//GEN-LAST:event_saveFileJMenuItemActionPerformed

