    public void showAboutBox() {
        if (aboutBox == null) {
            JFrame mainFrame = DailyNotesApp.getApplication().getMainFrame();
            aboutBox = new DailyNotesAboutBox(mainFrame);
            aboutBox.setLocationRelativeTo(mainFrame);
        }
        DailyNotesApp.getApplication().show(aboutBox);
    }

